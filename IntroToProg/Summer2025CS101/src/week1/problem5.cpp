#include <iostream>
using namespace std;
int main() {
    int num;
    cin>> num;
    num--;
    cout <<"num-- will result in "<< num<<endl;
    num*=7;
    cout <<"num*7 will result in "<< num<<endl;
    num-=14;
    cout <<"num-14 will result in "<< num<<endl;
    num/=12;
    cout <<"num/12 will result in "<< num<<endl;
    num+=3;
    cout <<"num+=3 will result in "<< num<<endl;
    int r=num%3;
    cout <<"r=num%3 will result in "<< r<<endl;
    return 0;
}//
